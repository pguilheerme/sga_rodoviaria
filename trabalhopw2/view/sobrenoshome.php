<h1>ATUALMENTE</h1>
<br>
<p class="paragrafotres">Hoje em dia, o Terminal Rodoviário de São Gonçalo conta com um grande número de profissionais capacitados, serviço de ótima qualidade, uma central de atendimento excelente e diversas opções de destinos para proporcionar um melhor passeio aos seus clientes. Atualmente, o Terminal Rodoviário de São Gonçalo do Amarante possui cerca de mil funcionários, oferece serviços para mais de 50 destinos no estado do Ceará, e em seu espaço circulam mais de mil passageiros mensalmente. Com projetos inovadores e uma gestão reconhecida pelo compromisso com a entrega de serviços de qualidade, a rodoviária torna-se uma das mais bem conceituadas do estado. </p>
