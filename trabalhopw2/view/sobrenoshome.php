<h1>FUNDAÇÃO</h1>
<br>
<p class="paragrafoum">O Terminal Rodoviário de São Gonçalo do Amarante foi inaugurado em 2006, após a prefeitura, em parceria com empresas de transportes fecharem uma parceria que dura até hoje e facilita a vida de muitos passageiros que moram no município. Após 6 anos anos sendo sediada em um local próximo a prefeitura municipal, finalmente iniciaram as obras para o grande terminal que teria sua localização na própria sede. Finalmente, em 2020, foi inaugurada a própria Central do Terminal Rodoviário de São Gonçalo, atendendo o pedido de muitos habitantes da região. Nessa mesma época, o terminal contou com novas parcerias de grandes empresas que contribuiram para a implementação de novos serviços para a rodoviária que tem o intuíto de avançar cada vez mais no ramo das viagens.</p>
</div>
